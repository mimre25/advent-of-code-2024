defmodule TwentyTest do
  use ExUnit.Case
  alias AdventOfCode.Twenty

  test "part_one" do
    assert Twenty.part_one(
             TestUtils.Input.str_to_matrix("""
             #####
             #...#
             #.#.#
             #S#E#
             #####
             """),
             2
           ) == 2

    assert Twenty.part_one(
             TestUtils.Input.str_to_matrix("""
             ###############
             #...#...#.....#
             #.#.#.#.#.###.#
             #S#...#.#.#...#
             #######.#.#.###
             #######.#.#...#
             #######.#.###.#
             ###..E#...#...#
             ###.#######.###
             #...###...#...#
             #.#####.#.###.#
             #.#...#.#.#...#
             #.#.#.#.#.#.###
             #...#...#...###
             ###############
             """),
             2
           ) == 44
  end

  test "part_two" do
    assert Twenty.part_two(
             TestUtils.Input.str_to_matrix("""
             ###############
             #...#...#.....#
             #.#.#.#.#.###.#
             #S#...#.#.#...#
             #######.#.#.###
             #######.#.#...#
             #######.#.###.#
             ###..E#...#...#
             ###.#######.###
             #...###...#...#
             #.#####.#.###.#
             #.#...#.#.#...#
             #.#.#.#.#.#.###
             #...#...#...###
             ###############
             """),
             50
           ) == 285
  end
end
